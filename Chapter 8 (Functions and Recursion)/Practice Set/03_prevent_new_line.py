#How do you prevent python print() function to print a new line at the end

#This prints each value in new line
print("Hey")
print("Watcha")
print("Doin")
print("Girl")

#To print in same line add (end="") after value in print statement
print("Hey", end="")
print("watcha", end ="")
print("Doin", end ="")
print("Girl", end ="")

print() #To print an empty line

#To print with space keep space in " "
print("Hey", end=" ")
print("watcha", end =" ")
print("Doin", end =" ")
print("Girl", end =" ")